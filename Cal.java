package epam.maven;

public class Cal {
	void add(int first,int second)
	{
		int result = first + second;
		System.out.println("Result = "+result);
	}
	void sub(int first,int second)
	{
		int result = first - second;
		System.out.println("Result = "+result);
	}	
	void mul(int first,int second)
	{
		int result = first * second;
		System.out.println("Result = "+result);
	}
	void div(int first,int second)
	{
		int result = first / second;
		System.out.println("Result = "+result);
	}
}
